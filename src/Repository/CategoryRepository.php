	/**
	 * @param null|string $word
	 */
	public function searchCategoriesQueryBuilder(?string $word): QueryBuilder
	{
		$queryBuilder = $this->createQueryBuilder('c');

		if ($word) {
			$queryBuilder
				->andWhere('c.name LIKE :word')
				->setParameter('word', '%'.$word.'%');
		}

		return $queryBuilder
			->orderBy('c.createdAt', 'DESC');
	}
